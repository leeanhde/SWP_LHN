    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        CourseDAO courseDAO = new CourseDAO();
        ArrayList<Course> listTop4Course = courseDAO.listTop4Course();

        CategoryDAO categoryDAO = new CategoryDAO();
        ArrayList<Category> listCategory = categoryDAO.selectAllWithCourseCount();

        UserDAO userDAO = new UserDAO();
       
        ArrayList<User> getTopTeachersWithMostCourses = userDAO.getTopTeachersWithMostCourses(5);

        request.setAttribute("listTop4Course", listTop4Course);
        request.setAttribute("listCategory", listCategory);
        request.setAttribute("TopTeachersWithMostCourses", getTopTeachersWithMostCourses);
        

        request.getRequestDispatcher("/admin/index.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        CourseDAO courseDAO = new CourseDAO();
        int userID = Integer.parseInt(request.getParameter("userID"));
        List<Course> userCourses = courseDAO.selectByUserID(userID);

        request.setAttribute("userCourseByID", userCourses);
        request.getRequestDispatcher("/admin/index.jsp").forward(request, response);
    }

