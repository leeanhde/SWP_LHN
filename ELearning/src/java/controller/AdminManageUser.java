    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        UserDAO userDAO = new UserDAO();
        ArrayList<User> listUsers = userDAO.selectAll();
        ArrayList<User> getTeacherOfCourse = userDAO.getTopTeachersWithMostCourses(100);

        LocalDate currentTime = userDAO.getCurrentDate();

        request.setAttribute("listUsers", listUsers);
        request.setAttribute("getTeacherOfCourse", getTeacherOfCourse);
        request.setAttribute("currentTime", currentTime);
        request.getRequestDispatcher("/admin/userManagement.jsp").forward(request, response);//userManagement
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        String txtSearch = request.getParameter("txtSearch");
        UserDAO userDAO = new UserDAO();

        ArrayList<User> matchingUsers = userDAO.searchUser(txtSearch);

        request.setAttribute("listUsers", matchingUsers);
        request.getRequestDispatcher("/admin/userManagement.jsp").forward(request, response);//userManagement
    }

